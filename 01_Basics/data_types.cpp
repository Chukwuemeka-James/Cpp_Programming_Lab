#include <iostream>
#include <string>


int main() {
    int integerNum = 42;
    float floatNum = 3.14f;
    double doubleNum = 2.718281828;
    char letter = 'C';
    bool isStudent = true;
    std::string text = "C++ is fun!";

    std::cout << "The Integer is: " << integerNum << '\n';
    std::cout << "The Float is: " << floatNum << '\n';
    std::cout << "The Double is: " << doubleNum << '\n';
    std::cout << "The Character is: " << letter << '\n';
    std::cout << "The Boolean is: " << isStudent << '\n';
    std::cout << "The String is: " << text << '\n';

    return 0;
}
